/*
A Case of Decrement For Loop:
    example: for(i = 5; i; i--){
                 printf("%d\n");
             }
    This for loop will keep on running until i become 0.
    The loop runs following steps:
        1. i is initialized to 5.
        2. The condition i (0 or non zero) is tested.
        3. The code is executed.
        4. i is decremented.
        5. Condition i is checked and code is executed if it's not 0.
        6. And so on until i is not 0
*/