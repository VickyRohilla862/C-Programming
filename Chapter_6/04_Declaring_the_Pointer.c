/*
The synatx for declaring a pointer is:
    int * ptr; or 
    int* ptr; or
    int *ptr;
example:
    int *j; // declare a variable j of type int-pointer
    j = &i; // stores the address of i to j
We can also declare pointers of different data types like:
    int, float, char, etc.
example:
    int *in_ptr;    // pointer to integer
    float *fl_ptr;  // pointer to float
    char *ch_ptr;   // pointer to character
*/